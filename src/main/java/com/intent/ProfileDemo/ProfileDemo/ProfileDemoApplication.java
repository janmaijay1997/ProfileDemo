package com.intent.ProfileDemo.ProfileDemo;

import com.intent.ProfileDemo.ProfileDemo.Controller.ProfileDemoController;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.context.ApplicationContext;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Profile;

@SpringBootApplication
public class ProfileDemoApplication {
	private static final Logger LOGGER= LogManager.getLogger(ProfileDemoApplication.class);

	public static void main(String[] args) {
		LOGGER.info("application has stated");
		ApplicationContext applicationContext=SpringApplication.run(ProfileDemoApplication.class, args);

		for (String name : applicationContext.getBeanDefinitionNames()) {
			System.out.println(name);
		}
	}

	@Profile("dev")
	@Bean
	public String devBean() {
		return "dev";
	}

	@Profile("qa")
	@Bean
	public String qaBean() {
		return "qa";
	}

	@Profile("prod")
	@Bean
	public String prodBean() {
		return "prod";
	}
}
